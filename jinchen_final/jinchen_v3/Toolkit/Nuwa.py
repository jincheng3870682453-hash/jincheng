if __name__ == "__main__":
    import argparse
    import sys

    logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")

    parser = argparse.ArgumentParser(description="Nuwa —— POC 报告 + 辐射检测")
    parser.add_argument("action", choices=["poc", "radiate", "graph"])
    parser.add_argument("--file", "-f", help="目标文件")
    parser.add_argument("--root", "-r", default=".", help="项目根目录")
    parser.add_argument("--output", "-o", default="poc_reports", help="输出目录")
    args = parser.parse_args()

    if args.action == "poc":
        n = Nuwa(title="命令行 POC", scenario="交互式创建")
        print("📝 输入测试步骤（空行结束）：")
        while True:
            line = input("  > ").strip()
            if not line:
                break
            n.add_step(line)
        n.set_result("步骤数", str(len(n.steps)))
        n.verdict = input("结论 (pass/fail/partial): ").strip() or "pass"
        paths = n.save(args.output)
        print(f"✅ 报告: {paths}")

    elif args.action == "radiate":
        if not args.file:
            print("❌ 请指定 --file")
            sys.exit(1)
        rd = RadiationDetector(project_root=args.root)
        alerts = rd.scan(args.file)
        print(rd.generate_report(alerts))

    elif args.action == "graph":
        rd = RadiationDetector(project_root=args.root)
        stats = rd.graph_stats()
        print(f"📊 文件数: {stats['files']}")
        print(f"📊 关系边数: {stats['edges']}")
        print(f"📊 平均依赖: {stats['avg_dependencies']}")
        print(f"\n🔗 关系示例（前 5 条）：")
        count = 0
        for src, dsts in list(rd._relation_graph.items())[:5]:
            for dst in dsts[:3]:
                print(f"  {src} → {dst}")
                count += 1
            if count >= 5:
                break
